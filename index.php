    <script type="text/javascript">
      function confirm(no_urut){
        $('#modal_confirm').modal('show');
        $("#hidden_val").val(no_urut);
      }

      function cancel(){
        $("#modal_confirm").modal('hide');
         location.reload();
      }

      function vote(){
          var no_urut = $("#hidden_val").val();
          $("#modal_confirm").modal('hide');
          $('#modal').modal('show');
          $("button").attr("disabled","disabled");
        


          $.ajax({
            url: "proses.php",
            data: "op=vote&no_urut="+no_urut,
            success:function(e){
              $("#hidden_val").val('');
            }
          })

          setTimeout(function(){ 
            $("button").removeAttr("disabled"); 
            $('#modal').modal('hide');
              location.reload();
          }, 
          3500);  
      }

